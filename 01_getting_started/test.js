// ไฟล์ทดสอบการทำงานของ Copilot ใน VS Code
// ลองพิมพ์โค้ดด้านล่างและดู Copilot แนะนำ

// ตัวอย่าง 1: ฟังก์ชันทักทาย
function greet(name) {
    // Copilot จะแนะนำการ return string
    return "Hello, " + name + "!";
}

// ตัวอย่าง 2: คำนวณพื้นที่วงกลม
function calculateCircleArea(radius) {
    // ลองพิมพ์ return แล้วดู Copilot แนะนำ
    return Math.PI * radius * radius;
}

// ตัวอย่าง 3: เช็คเลขคู่คี่
function isEven(number) {
    // Copilot จะแนะนำการเช็คเลขคู่คี่
    return number % 2 === 0;
}

// ตัวอย่าง 4: array operations
const numbers = [1, 2, 3, 4, 5];
// ลองพิมพ์ numbers. แล้วดู Copilot แนะนำ method ต่างๆ
numbers.forEach(num => {
    console.log(num);
});

// ตัวอย่าง 5: ลูป
for (let i = 0; i < 10; i++) {
    // พิมพ์ console. แล้วดู Copilot แนะนำ
    console.log(i);
}
